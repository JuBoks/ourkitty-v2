    # plt.show()

    # Code for Visualizing in Web-Front-End
    # 1) Calculate Width, Height
    x_max = 0
    x_min = 0
    y_max = 0
    y_min = 0

    for i in range(len(pca_features[:, 1])):
        if pca_features[i][0] > x_max:
            x_max = pca_features[i][0]
        if pca_features[i][0] < x_min:
            x_min = pca_features[i][0]
